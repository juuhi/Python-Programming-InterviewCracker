# Program to show the use of continue and break keyword inside loops and their difference

for val in "string":
    if val == "i":
        continue    #(to continue to the loop)
    print(val)

print("The end")

# Use of Break Keyword
for val in "string":
    if val == "i":
        break    #(to come out of the loop)
    print(val)

print("The end")